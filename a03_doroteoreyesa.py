    pelo.speed(10)
    pelo.penup()
    pelo.goto(-185, 200)
    pelo.pendown()
    for i in range(14):
        for i in range(2):
            pelo.begin_fill()
            pelo.forward(20)
            pelo.left(135)
            pelo.fd(20)
            pelo.left(90)
            pelo.end_fill()
