import turtle

def eyes():
    beto = turtle.Turtle()
    beto.penup()
    beto.goto(-100, 100)
    beto.pendown()
    beto.pensize(1)
    beto.speed(1000)
    beto.fillcolor("white")
    beto.begin_fill()
    beto.pencolor("black")
    for i in range(2):
        beto.forward(30)
        beto.right(45)
        beto.forward(5)
        beto.right(45)
        beto.forward(5)
        beto.right(45)
        beto.forward(5)
        beto.right(45)
        beto.forward(30)
    beto.right(90)
    beto.end_fill()
    beto.fillcolor("black")
    beto.begin_fill()
    for i in range(2):
        beto.forward(10)
        beto.left(90)
        beto.forward(10)
        beto.left(90)
    beto.end_fill()
    beto.penup()
    beto.goto(100, 100)
    beto.right(270)
    beto.pendown()
    beto.fillcolor("white")
